const { React, getModule } = require('powercord/webpack');
const Icon = require('./Icon');

module.exports = React.memo(
  (props) => {
    const { error, backgroundRed, icon, text } = getModule([ 'error', 'backgroundRed' ], false);
    const { marginBottom20 } = getModule([ 'marginBottom20' ], false);

    return (
      <div className={`${error} ${backgroundRed} ${marginBottom20}`}>
        <Icon className={icon} name='WarningCircle'/>
        <div className={text}>
          {props.children}
        </div>
      </div>
    );
  }
);
