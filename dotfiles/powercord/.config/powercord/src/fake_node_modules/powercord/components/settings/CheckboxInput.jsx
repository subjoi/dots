const { getModule, getModuleByDisplayName, React } = require('powercord/webpack');
const AsyncComponent = require('../AsyncComponent');
const Divider = require('../Divider');

const Checkbox = AsyncComponent.from(getModuleByDisplayName('Checkbox'));
const Flex = getModuleByDisplayName('Flex', false);
const { marginBottom20 } = getModule([ 'marginBottom20' ], false);

module.exports = React.memo(
  (props) => (
    <Flex className={marginBottom20} direction={Flex.Direction.VERTICAL}>
      <Checkbox {...props}/>
      <Divider/>
    </Flex>
  )
);
